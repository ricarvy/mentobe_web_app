// 完整的78张牌
export const allTarotCards: TarotCardData[] = [
  ...majorArcana,
  ...wandsCards,
  ...cupsCards,
  ...swordsCards,
  ...pentaclesCards,
];

// 按ID获取牌
export function getCardById(id: number): TarotCardData | undefined {
  return allTarotCards.find(card => card.id === id);
}

// 获取随机牌（用于抽牌）
export function drawRandomCards(count: number, includeReversed: boolean = true): Array<TarotCardData & { isReversed: boolean }> {
  const shuffled = [...allTarotCards].sort(() => Math.random() - 0.5);
  const drawn = shuffled.slice(0, count);

  return drawn.map(card => ({
    ...card,
    isReversed: includeReversed ? Math.random() > 0.5 : false,
  }));
}

// 按花色获取牌
export function getCardsBySuit(suit: 'Wands' | 'Cups' | 'Swords' | 'Pentacles'): TarotCardData[] {
  return allTarotCards.filter(card => card.suit === suit);
}

// 获取大阿卡纳
export function getMajorArcana(): TarotCardData[] {
  return majorArcana;
}

// 获取小阿卡纳
export function getMinorArcana(): TarotCardData[] {
  return [...wandsCards, ...cupsCards, ...swordsCards, ...pentaclesCards];
}
